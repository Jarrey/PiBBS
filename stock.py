URL = "http://finance.yahoo.com/d/quotes.csv?s={:s}&f=sl1d1t1cohgv"

def __get_stock_data__(stocks):
    data = httpget(URL.format('+'.join(stocks))).splitlines()
    rows = csv.reader(data, delimiter=',', skipinitialspace=True, quotechar='"')
    w = []
    for row in rows:
        # handle change string
        change = row[4].replace(' - ', ' ')
        w.append(['--{:s}--'.format(row[0]),
                  row[2],
                  'LT:{:s}'.format(row[1]),
                  'OP:{:s}'.format(row[5]),
                  change,
                  'Min:{0:s} Max:{1:s} Vol:{2:s}'.format(row[6], row[7], row[8])])
    return w

def show_stock(stocks = '/home/pi/stocks.ls'):
    lcd.draw_bitmap(STOCK_ICON)
    time.sleep(5)
    
    lines = [l.strip() for l in open(stocks, 'r')]

    for s in __get_stock_data__(lines):
        lcd_ext.marquee_display_words(s, 5)
        time.sleep(2)


if __name__ == "__main__":
    lcd.init()
    lcd.light(1)
    show_stock()
